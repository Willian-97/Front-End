// operadores de comparação
const idade = 28;
const maiorDeIdade = 18;
console.log("O aluno é maior de 18 ? ", idade > maiorDeIdade);
console.log("O aluno possui menos de 20 anos ? ", idade < 20);
console.log("A idade do aluno é maior ou igual 30 ? ", idade >= 30);
console.log("A idade do aluno é menor ou igual a 25? ", idade <= 28);
// operadores de igualdade
const x = 10;
const y = 20;
const z = "10";
console.log("O número " + x + " é igual ao número " + y, x === y);
console.log("O número " + x + " é igual ao número " + z, x === z);
console.log("O número " + x + " é igual ao número " + z, x == z);
console.log(`O numero ${x} é diferente do numero ${y}: ${x !== y}`);
console.log(`O numero ${x} é diferente do numero ${z}: ${x !== z}`);
